    def format_to_markdown(self, data: List[Any], course_name: str = "Course") -> str:
        """Format JSON data to Markdown format"""
        output = []
        now = datetime.now()
        
        # Header
        output.append("# Question Paper\n\n")
        output.append("**Generated by ScoriX AI System**\n\n")
        output.append(f"- **Date:** {now.strftime('%B %d, %Y')}\n")
        output.append(f"- **Time:** {now.strftime('%I:%M %p')}\n\n")
        output.append(f"## Course: {course_name}\n\n")
        output.append("---\n\n")
        
        # Extract questions
        if isinstance(data, (list, tuple)) and len(data) == 2:
            content_data, questions = data
        else:
            questions = data if isinstance(data, list) else []
        
        # Format each CO
        for idx, co_data in enumerate(questions, 1):
            if not isinstance(co_data, dict):
                continue
                
            co = co_data.get('CO', co_data.get('co', 'Unknown'))
            po = co_data.get('PO', co_data.get('po', []))
            topics = co_data.get('topics', [])
            co_questions = co_data.get('questions', [])
            
            output.append(f"## Course Outcome {idx}\n\n")
            output.append(f"**CO{idx}:** {co}\n\n")
            
            if po:
                output.append("### Program Outcomes (PO)\n\n")
                if isinstance(po, list):
                    for po_item in po:
                        output.append(f"- {po_item}\n")
                else:
                    output.append(f"- {po}\n")
                output.append("\n")
            
            if topics:
                output.append("### Topics Covered\n\n")
                for topic in topics:
                    output.append(f"- {topic}\n")
                output.append("\n")
            
            if co_questions:
                output.append(f"### Questions (Total: {len(co_questions)})\n\n")
                for q_idx, question in enumerate(co_questions, 1):
                    output.append(f"**Q{idx}.{q_idx}.** {question}\n\n")
            
            output.append("---\n\n")
        
        output.append("\n**End of Question Paper**\n")
        
        return ''.join(output)


