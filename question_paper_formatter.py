    def format_to_text(self, data: List[Any], course_name: str = "Course") -> str:
        """
        Format JSON data to readable text format
        
        Args:
            data: Question paper data (content_data, questions)
            course_name: Name of the course
            
        Returns:
            Formatted text string
        """
        output = []
        
        # Add header
        now = datetime.now()
        output.append(self.header_template.format(
            date=now.strftime("%B %d, %Y"),
            time=now.strftime("%I:%M %p")
        ))
        
        output.append(f"Course: {course_name}\n")
        output.append("="  * 80)
        output.append("\n")
        
        # Extract questions from data
        if isinstance(data, (list, tuple)) and len(data) == 2:
            content_data, questions = data
        else:
            questions = data if isinstance(data, list) else []
        
        # Format each CO section
        for idx, co_data in enumerate(questions, 1):
            if not isinstance(co_data, dict):
                continue
                
            co = co_data.get('CO', co_data.get('co', 'Unknown'))
            po = co_data.get('PO', co_data.get('po', []))
            topics = co_data.get('topics', [])
            co_questions = co_data.get('questions', [])
            
            # CO Header
            output.append(f"\n{'â”€' * 80}\n")
            output.append(f"COURSE OUTCOME {idx}\n")
            output.append(f"{'â”€' * 80}\n\n")
            output.append(f"CO{idx}: {co}\n\n")
            
            # Program Outcomes
            if po:
                output.append("Program Outcomes (PO):\n")
                if isinstance(po, list):
                    for po_item in po:
                        output.append(f"  â€¢ {po_item}\n")
                else:
                    output.append(f"  â€¢ {po}\n")
                output.append("\n")
            
            # Topics Covered
            if topics:
                output.append("Topics Covered:\n")
                for topic in topics:
                    output.append(f"  â€¢ {topic}\n")
                output.append("\n")
            
            # Questions
            if co_questions:
                output.append(f"QUESTIONS (Total: {len(co_questions)})\n")
                output.append(f"{'â”€' * 80}\n\n")
                
                for q_idx, question in enumerate(co_questions, 1):
                    output.append(f"Q{idx}.{q_idx}. {question}\n\n")
            
        # Footer
        output.append("\n" + "="  * 80)
        output.append("\n\nEND OF QUESTION PAPER\n")
        output.append("="  * 80)
        
        return ''.join(output)
    
